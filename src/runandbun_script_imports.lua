function getHP(mon)
    hptype = ((mon.hpIV%2 + (2*(mon.attackIV%2))+(4*(mon.defenseIV%2))+(8*(mon.speedIV%2))+(16*(mon.spAttackIV%2))+(32*(mon.spDefenseIV%2)))*5)/21
    hptype = math.floor(hptype)
	if (hptype == 0) then
		return "Combat"
	end
	if (hptype == 1) then
		return "Vol"
	end
	if (hptype == 2) then
		return "Poison"
	end
	if (hptype == 3) then
		return "Sol"
	end
	if (hptype == 4) then
		return "Roche"
	end
	if (hptype ==5) then
		return "Insecte"
	end
	if (hptype == 6) then
		return "Spectre"
	end
	if (hptype ==7) then
		return "Acier"
	end
	if (hptype == 8) then
		return "Feu"
	end
	if (hptype == 9) then
		return "Eau"
	end
	if (hptype == 10) then
		return "Plante"
	end
	if (hptype == 11) then
		return "Electrik"
	end
	if (hptype == 12) then
		return "Psy"
	end
	if (hptype == 13) then
		return "Glace"
	end
	if (hptype == 14) then
		return "Dragon"
